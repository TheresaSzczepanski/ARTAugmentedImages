public class Vector4 {


    int x;
    int y;
    int z;
    float q;


    public Vector4() {

        x = 0;
        y = 0;
        z = 0;
        q = 0;

    }


    public Vector4(int myx, int myy, int myz, float myq){


        x = myx;
        y = myy;
        z = myz;
        q = myq;

    }

}
